import type { ThesisProcess, ThesisTask } from "@/types/thesis";

type TaskSeed = readonly [title: string, description: string];

function createTask(
  processOrder: number,
  taskOrder: number,
  seed: TaskSeed,
): ThesisTask {
  const [title, description] = seed;

  return {
    id: `process-${String(processOrder).padStart(2, "0")}-task-${String(taskOrder).padStart(2, "0")}`,
    order: taskOrder,
    title,
    description,
    status: "not_started",
    progress: 0,
    deadline: null,
    memo: "",
    isInitial: true,
  };
}

function createProcess(
  order: number,
  title: string,
  description: string,
  taskSeeds: readonly TaskSeed[],
): ThesisProcess {
  return {
    id: `process-${String(order).padStart(2, "0")}`,
    order,
    title,
    description,
    tasks: taskSeeds.map((seed, index) => createTask(order, index + 1, seed)),
  };
}

